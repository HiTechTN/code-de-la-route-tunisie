// Utility functions
export const getQuestionsByCategory = (categoryId: string): Question[] => {
  return QUESTIONS.filter(q => q.category === categoryId);
};

export const getQuestionsByDifficulty = (difficulty: 'facile' | 'moyen' | 'difficile'): Question[] => {
  return QUESTIONS.filter(q => q.difficulty === difficulty);
};

export const getRandomQuestions = (count: number, categoryId?: string): Question[] => {
  let pool = categoryId ? getQuestionsByCategory(categoryId) : [...QUESTIONS];
  const shuffled = pool.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, shuffled.length));
};

export const getExamQuestions = (): Question[] => {
  // Simulate official exam: 30 questions randomly selected
  return getRandomQuestions(30);
};

export const getCategoryStats = () => {
  return CATEGORIES.map(cat => ({
    ...cat,
    totalQuestions: getQuestionsByCategory(cat.id).length,
    easy: getQuestionsByCategory(cat.id).filter(q => q.difficulty === 'facile').length,
    medium: getQuestionsByCategory(cat.id).filter(q => q.difficulty === 'moyen').length,
    hard: getQuestionsByCategory(cat.id).filter(q => q.difficulty === 'difficile').length,
  }));
};
